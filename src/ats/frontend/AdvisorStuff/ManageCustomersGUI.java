    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        
        try {
            String[] uname,lname,address,email,dob,type,passport,uphone; float[] disco;
            if(!"".equals(search.getText()) && !"Search".equals(search.getText()))
            {
                uname =db.get1String("SELECT firstname FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                lname =db.get1String("SELECT lastname FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                address =db.get1String("SELECT adress FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                email =db.get1String("SELECT email FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                dob =db.get1String("SELECT dobirth FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                type =db.get1String("SELECT type FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                passport =db.get1String("SELECT passportno FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                uphone =db.get1String("SELECT phone FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                disco =db.get1FLOAT("SELECT discount FROM ats_sysdb.customer WHERE lastname LIKE '%"+search.getText()+"%'");
                
            }
            else
            {
                
                uname =db.get1String("SELECT firstname FROM ats_sysdb.customer");
                lname =db.get1String("SELECT lastname FROM ats_sysdb.customer");
                address =db.get1String("SELECT adress FROM ats_sysdb.customer");
                email =db.get1String("SELECT email FROM ats_sysdb.customer");
                dob =db.get1String("SELECT dobirth FROM ats_sysdb.customer");
                type =db.get1String("SELECT type FROM ats_sysdb.customer");
                passport =db.get1String("SELECT passportno FROM ats_sysdb.customer");
                uphone =db.get1String("SELECT phone FROM ats_sysdb.customer");
                disco =db.get1FLOAT("SELECT discount FROM ats_sysdb.customer");
                
            }
            int i=0;
            DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
            int rowCount = model.getRowCount();

            for (int x = rowCount - 1; x >= 0; x--) 
                model.removeRow(i);
        
            while(lname[i]!=null)
            {
                model.addRow(new Object[]{uname[i] , lname[i] , address[i] , email[i] , dob[i] , passport[i] , uphone[i] , disco[i] , type[i] });
                //userTable.getModel().setValueAt(s[i],i,0);
                //userTable.getModel().setValueAt(n[i],i,1);
                //userTable.getModel().setValueAt(r[i],i,2);
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminGUI.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
        
    }//GEN-LAST:event_findActionPerformed

