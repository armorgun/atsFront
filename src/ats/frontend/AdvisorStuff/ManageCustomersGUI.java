    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            // TODO add your handling code here:
            String fn,ln,ad,em,dob,pp,pn,ty; float dc;
            
            fn=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 0);
            ln=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 1);
            ad=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 2);
            em=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 3);
            dob=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 4);
            pn=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 6);
            pp=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 5);
            ty=(String)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 8);
            dc=(float)customerTable.getModel().getValueAt(customerTable.getSelectedRow(), 7);
            
            

            db.statement("UPDATE `ats_sysdb`.`customer` SET `passportno`='"+pp+"', `firstname`='"+fn+"', `lastname`='"+ln+"', `adress`='"+ad+"', `email`='"+em+"', `dobirth`='"+dob+"', `phone`='"+pn+"', `type`='"+ty+"', `discount`='"+dc+"' WHERE `passportno`='"+pp+"'");
        } catch (SQLException ex) {
            Logger.getLogger(ManageCustomersGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        
    }//GEN-LAST:event_saveActionPerformed

